def train_model(model, train_loader, valid_loader, epochs=30, lr=1e-4):
    """Обучение с логированием и сохранением лучшей модели"""

    model = model.to(device)

    optimizer = optim.AdamW(model.parameters(), lr=lr, weight_decay=1e-4)
    scheduler = optim.lr_scheduler.CosineAnnealingWarmRestarts(
        optimizer, T_0=10, eta_min=1e-6)
    criterion = nn.CrossEntropyLoss()
    scaler = GradScaler('cuda')  # ← Исправлено

    best_val_acc = 0.0
    history = {
        'train_loss': [], 'train_acc': [],
        'val_loss': [], 'val_acc': []
    }

    for epoch in range(epochs):
        # ========== TRAIN ==========
        model.train()
        train_loss, train_correct, train_total = 0, 0, 0

        pbar = tqdm(train_loader, desc=f"Epoch {epoch+1}/{epochs}")
        for images, bio, labels in pbar:
            images = images.to(device, non_blocking=True)
            bio = bio.to(device, non_blocking=True)
            labels = labels.to(device, non_blocking=True)

            optimizer.zero_grad(set_to_none=True)

            with autocast('cuda'):  # ← Исправлено
                outputs = model(images, bio)
                loss = criterion(outputs, labels)

            scaler.scale(loss).backward()
            scaler.step(optimizer)
            scaler.update()

            train_loss += loss.item()
            _, predicted = torch.max(outputs, 1)
            train_total += labels.size(0)
            train_correct += (predicted == labels).sum().item()

            pbar.set_postfix({
                'loss': f'{loss.item():.4f}',
                'acc': f'{100*train_correct/train_total:.1f}%'
            })

        train_loss /= len(train_loader)
        train_acc = 100 * train_correct / train_total

        # ========== VALIDATION ==========
        model.eval()
        val_loss, val_correct, val_total = 0, 0, 0

        with torch.no_grad():
            for images, bio, labels in valid_loader:
                images = images.to(device, non_blocking=True)
                bio = bio.to(device, non_blocking=True)
                labels = labels.to(device, non_blocking=True)

                with autocast('cuda'):
                    outputs = model(images, bio)
                    loss = criterion(outputs, labels)

                val_loss += loss.item()
                _, predicted = torch.max(outputs, 1)
                val_total += labels.size(0)
                val_correct += (predicted == labels).sum().item()

        val_loss /= len(valid_loader)
        val_acc = 100 * val_correct / val_total

        scheduler.step()

        # Logging
        history['train_loss'].append(train_loss)
        history['train_acc'].append(train_acc)
        history['val_loss'].append(val_loss)
        history['val_acc'].append(val_acc)

        print(
            f"\n✅ Epoch {epoch+1}: Train {train_acc:.1f}% | Val {val_acc:.1f}% | LR: {scheduler.get_last_lr()[0]:.2e}")

        # Save best
        if val_acc > best_val_acc:
            best_val_acc = val_acc
            torch.save(model.state_dict(),
                       '/content/drive/MyDrive/best_mobilenet_bio.pth')
            print(f"🏆 New Best: {best_val_acc:.1f}%")

    return history

