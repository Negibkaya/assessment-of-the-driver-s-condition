def evaluate_model(model, test_loader):
    """Полная оценка модели на тестовом наборе"""

    model.eval()
    model.to(device)

    all_preds = []
    all_labels = []
    all_probs = []
    fusion_weights_visual = []
    fusion_weights_bio = []

    with torch.no_grad():
        for images, bio, labels in tqdm(test_loader, desc="Testing"):
            images = images.to(device)
            bio = bio.to(device)

            outputs = model(images, bio)
            probs = torch.softmax(outputs, dim=1)
            _, predicted = torch.max(outputs, 1)

            weights = model.get_fusion_weights(images, bio)

            all_preds.extend(predicted.cpu().numpy())
            all_labels.extend(labels.numpy())
            all_probs.extend(probs[:, 1].cpu().numpy())
            fusion_weights_visual.extend(weights[:, 0].cpu().numpy())
            fusion_weights_bio.extend(weights[:, 1].cpu().numpy())

    return {
        'preds': np.array(all_preds),
        'labels': np.array(all_labels),
        'probs': np.array(all_probs),
        'fusion_visual': np.array(fusion_weights_visual),
        'fusion_bio': np.array(fusion_weights_bio)
    }


