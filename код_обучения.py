def parse_yolo_label(label_path, img_width, img_height):
    """
    Парсит YOLO label файл и возвращает список bbox'ов

    YOLO формат: class_id x_center y_center width height (всё нормализовано 0-1)
    """
    boxes = []

    if not os.path.exists(label_path):
        return boxes

    with open(label_path, 'r') as f:
        for line in f:
            parts = line.strip().split()
            if len(parts) < 5:
                continue

            class_id = int(parts[0])
            x_center = float(parts[1])
            y_center = float(parts[2])
            width = float(parts[3])
            height = float(parts[4])

            # Конвертация из нормализованных в пиксельные координаты
            x_center_px = x_center * img_width
            y_center_px = y_center * img_height
            width_px = width * img_width
            height_px = height * img_height

            # Конвертация в (x1, y1, x2, y2)
            x1 = int(x_center_px - width_px / 2)
            y1 = int(y_center_px - height_px / 2)
            x2 = int(x_center_px + width_px / 2)
            y2 = int(y_center_px + height_px / 2)

            boxes.append({
                'class_id': class_id,
                'bbox': (x1, y1, x2, y2),
                'yolo_class': YOLO_CLASS_NAMES[class_id] if class_id < len(YOLO_CLASS_NAMES) else 'unknown'
            })

    return boxes


def expand_bbox(bbox, img_width, img_height, padding_ratio=0.2):
    """Расширяет bbox для захвата контекста"""
    x1, y1, x2, y2 = bbox

    w = x2 - x1
    h = y2 - y1

    pad_w = int(w * padding_ratio)
    pad_h = int(h * padding_ratio)

    x1 = max(0, x1 - pad_w)
    y1 = max(0, y1 - pad_h)
    x2 = min(img_width, x2 + pad_w)
    y2 = min(img_height, y2 + pad_h)

    return (x1, y1, x2, y2)

