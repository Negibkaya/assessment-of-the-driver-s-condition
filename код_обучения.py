def convert_yolo_to_classification(yolo_path, output_path, use_crops=True):
    """Конвертирует YOLO датасет в классификационный"""

    yolo_path = Path(yolo_path)
    output_path = Path(output_path)

    # Очищаем output если существует
    if output_path.exists():
        shutil.rmtree(output_path)

    stats = {'total': 0, 'success': 0, 'failed': 0, 'per_class': Counter()}

    for split in ['train', 'valid', 'test']:
        images_dir = yolo_path / split / 'images'
        labels_dir = yolo_path / split / 'labels'

        if not images_dir.exists():
            print(f"⚠️ Папка {images_dir} не найдена, пропускаем...")
            continue

        print(f"\n📂 Обработка {split}...")

        # Создаём выходные папки
        for class_name in CLASS_NAMES:
            (output_path / split / class_name).mkdir(parents=True, exist_ok=True)

        # Обрабатываем изображения
        image_files = list(images_dir.glob('*.jpg')) + \
            list(images_dir.glob('*.png'))

        for img_path in tqdm(image_files, desc=f"  {split}"):
            stats['total'] += 1

            img = cv2.imread(str(img_path))
            if img is None:
                stats['failed'] += 1
                continue

            img_height, img_width = img.shape[:2]

            label_path = labels_dir / (img_path.stem + '.txt')
            boxes = parse_yolo_label(label_path, img_width, img_height)

            if len(boxes) == 0:
                stats['failed'] += 1
                continue

            for i, box_info in enumerate(boxes):
                yolo_class_id = box_info['class_id']

                if yolo_class_id not in CLASS_MAPPING:
                    continue

                classification_class = CLASS_MAPPING[yolo_class_id]
                class_name = CLASS_NAMES[classification_class]

                if use_crops:
                    bbox = box_info['bbox']
                    bbox = expand_bbox(
                        bbox, img_width, img_height, PADDING_RATIO)
                    x1, y1, x2, y2 = bbox

                    if (x2 - x1) < MIN_BBOX_SIZE or (y2 - y1) < MIN_BBOX_SIZE:
                        continue

                    crop = img[y1:y2, x1:x2]
                    crop = cv2.resize(crop, (OUTPUT_SIZE, OUTPUT_SIZE))
                    output_img = crop
                else:
                    output_img = cv2.resize(img, (OUTPUT_SIZE, OUTPUT_SIZE))

                output_filename = f"{img_path.stem}_box{i}.jpg"
                output_filepath = output_path / split / class_name / output_filename

                cv2.imwrite(str(output_filepath), output_img)

                stats['success'] += 1
                stats['per_class'][f"{split}_{class_name}"] += 1

    # Статистика
    print("\n" + "=" * 60)
    print("📊 СТАТИСТИКА КОНВЕРТАЦИИ")
    print("=" * 60)
    print(f"Всего изображений: {stats['total']}")
    print(f"Успешно: {stats['success']}")
    print(f"Пропущено: {stats['failed']}")
    print("\nПо классам:")

    for split in ['train', 'valid', 'test']:
        print(f"\n  {split}:")
        for class_name in CLASS_NAMES:
            key = f"{split}_{class_name}"
            count = stats['per_class'].get(key, 0)
            print(f"    {class_name}: {count}")

    return output_path

