class MobileNetV3BiometricFusion(nn.Module):
    """MobileNetV3-Large + Биометрические признаки с Attention Fusion"""

    def __init__(self, num_bio_features=8, hidden_dim=256, num_classes=2, dropout=0.4):
        super().__init__()

        # Visual Backbone
        self.backbone = models.mobilenet_v3_large(weights='IMAGENET1K_V1')
        backbone_out = self.backbone.classifier[0].in_features  # 960
        self.backbone.classifier = nn.Identity()

        # Visual Projector
        self.visual_proj = nn.Sequential(
            nn.Linear(backbone_out, hidden_dim),
            nn.BatchNorm1d(hidden_dim),
            nn.ReLU(),
            nn.Dropout(dropout)
        )

        # Biometric Encoder
        self.bio_encoder = nn.Sequential(
            nn.Linear(num_bio_features, 64),
            nn.BatchNorm1d(64),
            nn.ReLU(),
            nn.Dropout(0.2),
            nn.Linear(64, 128),
            nn.BatchNorm1d(128),
            nn.ReLU(),
            nn.Dropout(0.2),
            nn.Linear(128, hidden_dim),
            nn.BatchNorm1d(hidden_dim),
            nn.ReLU()
        )

        # Adaptive Fusion Gate
        self.fusion_gate = nn.Sequential(
            nn.Linear(hidden_dim * 2, 64),
            nn.ReLU(),
            nn.Linear(64, 2),
            nn.Softmax(dim=1)
        )

        # Classifier
        self.classifier = nn.Sequential(
            nn.Linear(hidden_dim, 128),
            nn.BatchNorm1d(128),
            nn.ReLU(),
            nn.Dropout(dropout),
            nn.Linear(128, num_classes)
        )

    def forward(self, image, bio_features):
        v = self.visual_proj(self.backbone(image))
        b = self.bio_encoder(bio_features)

        concat = torch.cat([v, b], dim=1)
        weights = self.fusion_gate(concat)

        fused = weights[:, 0:1] * v + weights[:, 1:2] * b

        return self.classifier(fused)

    def get_fusion_weights(self, image, bio_features):
        """Для анализа: какой модальности модель доверяет больше"""
        v = self.visual_proj(self.backbone(image))
        b = self.bio_encoder(bio_features)
        concat = torch.cat([v, b], dim=1)
        return self.fusion_gate(concat)


