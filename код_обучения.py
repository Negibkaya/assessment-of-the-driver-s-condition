class RobustBiometricExtractor:
    """Извлечение биометрических признаков с MediaPipe"""

    def __init__(self):
        self.face_mesh = mp.solutions.face_mesh.FaceMesh(
            static_image_mode=True,
            max_num_faces=1,
            refine_landmarks=True,
            min_detection_confidence=0.3
        )

        self.LEFT_EYE = [362, 385, 387, 263, 373, 380]
        self.RIGHT_EYE = [33, 160, 158, 133, 153, 144]

    def _distance(self, p1, p2):
        return np.sqrt((p1.x - p2.x)**2 + (p1.y - p2.y)**2)

    def _calculate_ear(self, landmarks, indices):
        try:
            pts = [landmarks[i] for i in indices]
            v1 = self._distance(pts[1], pts[5])
            v2 = self._distance(pts[2], pts[4])
            h = self._distance(pts[0], pts[3])
            if h < 0.01:
                return 0.0
            ear = (v1 + v2) / (2.0 * h)
            return ear if 0.05 < ear < 0.5 else 0.0
        except:
            return 0.0

    def _calculate_mar(self, landmarks):
        try:
            v = self._distance(landmarks[13], landmarks[14])
            h = self._distance(landmarks[61], landmarks[291])
            if h < 0.01:
                return 0.0
            return v / h
        except:
            return 0.0

    def _calculate_pose(self, landmarks):
        try:
            nose = landmarks[1]
            left_eye = landmarks[263]
            right_eye = landmarks[33]

            mid_x = (left_eye.x + right_eye.x) / 2
            mid_y = (left_eye.y + right_eye.y) / 2

            yaw = (nose.x - mid_x) * 100
            pitch = (nose.y - mid_y) * 100
            roll = (left_eye.y - right_eye.y) * 100

            return yaw, pitch, roll
        except:
            return 0.0, 0.0, 0.0

    def extract(self, image_bgr):
        """Возвращает 8 биометрических признаков"""
        features = np.zeros(8, dtype=np.float32)

        if image_bgr is None:
            return features

        image_rgb = cv2.cvtColor(image_bgr, cv2.COLOR_BGR2RGB)
        results = self.face_mesh.process(image_rgb)

        if results.multi_face_landmarks:
            lm = results.multi_face_landmarks[0].landmark

            l_ear = self._calculate_ear(lm, self.LEFT_EYE)
            r_ear = self._calculate_ear(lm, self.RIGHT_EYE)
            mar = self._calculate_mar(lm)
            yaw, pitch, roll = self._calculate_pose(lm)

            features[0] = l_ear
            features[1] = r_ear
            features[2] = (l_ear + r_ear) / 2.0
            features[3] = mar
            features[4] = yaw
            features[5] = pitch
            features[6] = roll
            features[7] = 1.0 if l_ear > 0 else 0.0

        return features


