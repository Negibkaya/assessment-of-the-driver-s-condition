def precompute_biometrics(dataset_path, cache_path):
    """
    Извлекает биометрию из ВСЕХ изображений ОДИН РАЗ и сохраняет в файл.
    Это занимает 2-5 минут, но потом обучение будет быстрым!
    """

    cache = {}
    dataset_path = Path(dataset_path)

    total_images = 0
    successful = 0

    for split in ['train', 'valid', 'test']:
        for class_name in CLASS_NAMES:
            class_dir = dataset_path / split / class_name
            if not class_dir.exists():
                continue

            images = list(class_dir.glob('*.jpg')) + \
                list(class_dir.glob('*.png'))

            for img_path in tqdm(images, desc=f"📊 {split}/{class_name}"):
                total_images += 1
                img = cv2.imread(str(img_path))

                if img is not None:
                    features = bio_extractor.extract(img)
                    cache[str(img_path)] = features
                    if features[7] > 0:  # Face detected
                        successful += 1
                else:
                    cache[str(img_path)] = np.zeros(8, dtype=np.float32)

    # Сохраняем кэш
    with open(cache_path, 'wb') as f:
        pickle.dump(cache, f)

    print(f"\n✅ Кэш биометрии создан:")
    print(f"   Всего изображений: {total_images}")
    print(
        f"   Лицо найдено: {successful} ({100*successful/total_images:.1f}%)")
    print(f"   Сохранено в: {cache_path}")

    return cache

