class FastFatigueDataset(Dataset):
    """
    Оптимизированный Dataset - биометрия берётся из кэша!
    Это делает обучение в 20-50 раз быстрее.
    """

    def __init__(self, root_dir, split, transform, bio_cache):
        self.root_dir = Path(root_dir) / split
        self.transform = transform
        self.bio_cache = bio_cache
        self.samples = []

        # Загружаем все изображения
        for class_idx, class_name in enumerate(CLASS_NAMES):
            class_dir = self.root_dir / class_name
            if class_dir.exists():
                for img_path in class_dir.glob('*.*'):
                    if img_path.suffix.lower() in ['.jpg', '.jpeg', '.png']:
                        self.samples.append({
                            'path': str(img_path),
                            'label': class_idx
                        })

        # Статистика
        labels = [s['label'] for s in self.samples]
        counter = Counter(labels)
        print(f"[{split}] Загружено {len(self.samples)} изображений")
        for idx, name in enumerate(CLASS_NAMES):
            print(f"  - {name}: {counter.get(idx, 0)}")

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        sample = self.samples[idx]

        # Загрузка изображения
        img = cv2.imread(sample['path'])
        if img is None:
            img = np.zeros((224, 224, 3), dtype=np.uint8)

        # ⚡ Биометрия ИЗ КЭША (мгновенно!)
        bio = self.bio_cache.get(sample['path'], np.zeros(8, dtype=np.float32))

        # Конвертация BGR → RGB для PyTorch
        img_rgb = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
        img_pil = Image.fromarray(img_rgb)

        # Применяем трансформации
        if self.transform:
            img_tensor = self.transform(img_pil)
        else:
            img_tensor = transforms.ToTensor()(img_pil)

        return img_tensor, torch.tensor(bio, dtype=torch.float32), sample['label']

